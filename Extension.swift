import Foundation

extension Float
{
    func currency() -> String
    {
        return "$ \(self)"
    }
    
}

extension Double
{
    func Currency() -> String
    {
        return "$ \(self)"
    }
    
}

extension Int
{
    func currencyI() -> String
    {
        return "$ \(self)"
    }
    
}
